# Set

my_set = {1, 2, 3, 4, 5}
print(type(my_set))
print("Original Set : ", my_set) 
       
print("Accessing elements is not possible in Set due to its unordered nature.")
print("Length of Set : ", len(my_set))

print("Adding 6 to Set")
my_set.add(6)
print("Updated Set : ", my_set)

print("Removing 2 from Set")
my_set.remove(2)
print("Final Set : ", my_set)

print("Reversed Set is not applicable as Set is unordered.")