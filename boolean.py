print("a AND b : ", a and b)
print("a OR b : ", a or b)

print("NOT a : ", not a)
print("NOT b : ", not b)

print("a EQUALS b : ", a == b)
print("a NOT EQUALS b : ", a != b)

print("a is b : ", a is b)
print("a is not b : ", a is not b)

print("Integer value of a : ", int(a))
print("Integer value of b : ", int(b))
  
print("Float value of a : ", float(a))
print("Float value of b : ", float(b))
  
print("String value of a : ", str(a))
print("String value of b : ", str(b))

print("Boolean value of 1 : ", bool(1))
print("Boolean value of 0 : ", bool(0))
print("Boolean value of -1 : ", bool(-1))
